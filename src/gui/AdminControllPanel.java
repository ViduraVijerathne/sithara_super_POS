    private void loadCacherTable() {
        cashiers = controller.getCashiers();
        DefaultTableModel model = (DefaultTableModel) cachiersTable.getModel();
        model.setRowCount(0); // Clear the existing table data
        for (User cashier : cashiers) {
            Object[] row = {
                cashier.getEmail(),
                cashier.getPassword(),
                cashier.getName()
            };
            model.addRow(row);
        }

    }

    private void loadUnitTable() {
        units = controller.getAllUnits();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); // Clear the existing table data
        for (Units unit : units) {
            Object[] row = {
                unit.getId(),
                unit.getUnit()
            };
            model.addRow(row);
        }
    }

    private void initData() {
        loadCacherTable();
        loadUnitTable();

    }

