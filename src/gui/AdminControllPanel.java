    private void cachiersTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cachiersTableMouseClicked
        // TODO add your handling code here:

        int rowIndex = cachiersTable.getSelectedRow();
        if (rowIndex != -1) {
            String email = (String) cachiersTable.getValueAt(rowIndex, 0);
            for (User cashier : cashiers) {
                if (cashier.getEmail().equals(email)) {
                    selectedCashier = cashier;
                    System.out.println("Done");
                    break;
                }
            }
        }

    }//GEN-LAST:event_cachiersTableMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (selectedCashier == null) {
            JOptionPane.showMessageDialog(this, "Please Select Cashire to delete", "Oops!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int showConfirmDialog = JOptionPane.showConfirmDialog(this, "Are you sure Delete " + selectedCashier.getName() + "?", "Warning!", JOptionPane.WARNING_MESSAGE);
        if (showConfirmDialog == 0) {
            try {
                controller.deleteCashier(selectedCashier);
                JOptionPane.showMessageDialog(this, "successfully deleted!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                refresh();
            } catch (Exception ex) {
                System.out.println(ex.toString());
                JOptionPane.showMessageDialog(this, "something went wrong  try again", "Oops!", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = tf_name.getText();
        String email = tf_email.getText();
        String password = tf_password.getText();
        if (Validator.isValidName(name)) {
            if (Validator.isValidEmail(email)) {
                if (Validator.isValidPassword(password)) {
                    for (User cashier : cashiers) {
                        if (cashier.getEmail().equals(email)) {
                            JOptionPane.showMessageDialog(this, "email already exist", "Oops!", JOptionPane.ERROR_MESSAGE);
                            return;

                        }

                    }
                    User cashier = new User();
                    cashier.setEmail(email);
                    cashier.setPassword(password);
                    cashier.setName(name);
                    cashier.setRole(Roles.CASHIER);
                    try {
                        controller.addCashier(cashier);
                        JOptionPane.showMessageDialog(this, "successfully Added!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                        refresh();
                        tf_name.setText("");
                        tf_email.setText("");
                        tf_password.setText("");
                    } catch (Exception ex) {
                        System.out.println(ex.toString());
                        JOptionPane.showMessageDialog(this, "something went wrong  try again", "Oops!", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String unitname = ft_unitName.getText();

        for (Units unit : units) {
            if (unit.getUnit().equals(unitname)) {
                JOptionPane.showMessageDialog(this, "unit already exist", "Oops!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        Units u = new Units();
        u.setUnit(unitname);
        try {
            controller.addUnit(u);
            JOptionPane.showMessageDialog(this, "successfully Added!", "Success!", JOptionPane.INFORMATION_MESSAGE);
            refresh();
            ft_unitName.setText("");
        } catch (Exception ex) {
            System.out.println(ex.toString());
            JOptionPane.showMessageDialog(this, "something went wrong  try again:" + ex.getMessage(), "Oops!", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int rowIndex = jTable2.getSelectedRow();
        if (rowIndex != -1) {
            int id = (int) jTable2.getValueAt(rowIndex, 0);
            for (Units unit : units) {
                if (unit.getId() == id) {
                    jButton4.setEnabled(false);
                    jButton5.setEnabled(true);
                    ft_unitName.setText(unit.getUnit());
                    selectedUnit = unit;
                }
            }
        }

    }//GEN-LAST:event_jTable2MouseClicked

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (selectedUnit == null) {
            JOptionPane.showMessageDialog(this, "please select a Unit to update!", "OOps!", JOptionPane.ERROR_MESSAGE);
        } else {
            String unitName = ft_unitName.getText();
            if (unitName.isBlank()) {
                JOptionPane.showMessageDialog(this, "please add Valid unit name", "OOps!", JOptionPane.ERROR_MESSAGE);
            } else {
                selectedUnit.setUnit(unitName);
                try {
                    controller.updateUnit(selectedUnit);
                    JOptionPane.showMessageDialog(this, "successfull  Unit update!", "success", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "OOps!", JOptionPane.ERROR_MESSAGE);
                }
            }

        }

        jButton4.setEnabled(true);
        jButton5.setEnabled(false);
        ft_unitName.setText("");
        selectedUnit = null;

        refresh();
    }//GEN-LAST:event_jButton5ActionPerformed

